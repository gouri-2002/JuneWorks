#relational operators=(<,<=,>,>=,==,!=)

num1=100
num2=200

print(num1<num2)
print(num1<=num2)
print(num1>num2)
print(num1>=num2)
print(num1==num2)
print(num1!=0) # num1 is either +ve or -ve

print(num1%2==0) #num1 is even
print(num2%2!=0) #num2 is odd
print(num1%100==0) # last 2 digits of num1 is 00
